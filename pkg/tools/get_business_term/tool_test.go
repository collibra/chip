func TestGetBusinessTerm_FiltersNonPhysicalData(t *testing.T) {
	mux := http.NewServeMux()

	mux.Handle("GET /rest/2.0/attributes", testutil.JsonHandlerOut(
		func(r *http.Request) (int, clients.GetBusinessTermAttributesResponse) {
			return http.StatusOK, clients.GetBusinessTermAttributesResponse{Total: 0, Results: []clients.GetBusinessTermAttributeResponse{}}
		},
	))

	mux.Handle("GET /rest/2.0/assets/{id}", testutil.JsonHandlerOut(
		func(r *http.Request) (int, clients.GetBusinessTermAssetResponse) {
			return http.StatusOK, clients.GetBusinessTermAssetResponse{
				ID:     "bt-filter",
				Name:   "Filtered Term",
				Type:   clients.GetBusinessTermAssetType{ID: "type-bt", Name: "Business Term"},
				Domain: clients.GetBusinessTermDomain{ID: "dom-001", Name: "Sales"},
				Status: clients.GetBusinessTermStatus{ID: "st-001", Name: "Approved"},
			}
		},
	))

	mux.Handle("GET /rest/2.0/relations", testutil.JsonHandlerOut(
		func(r *http.Request) (int, clients.GetBusinessTermRelationsResponse) {
			sourceID := r.URL.Query().Get("sourceId")
			switch sourceID {
			case "bt-filter":
				// Return a mix of physical and non-physical relation targets.
				return http.StatusOK, clients.GetBusinessTermRelationsResponse{
					Total: 3,
					Results: []clients.GetBusinessTermRelation{
						newRelation("rel-a", "bt-filter", "Filtered Term", "Business Term",
							"da-100", "Good DA", "Data Attribute", "rtype-1", "describes"),
						newRelation("rel-b", "bt-filter", "Filtered Term", "Business Term",
							"rpt-001", "Some Report", "Report", "rtype-x", "generates"),
						newRelation("rel-c", "bt-filter", "Filtered Term", "Business Term",
							"pol-001", "Some Policy", "Data Policy", "rtype-y", "governedBy"),
					},
				}
			case "da-100":
				// Data Attribute has a Table and a non-physical target.
				return http.StatusOK, clients.GetBusinessTermRelationsResponse{
					Total: 2,
					Results: []clients.GetBusinessTermRelation{
						newRelation("rel-d", "da-100", "Good DA", "Data Attribute",
							"tbl-100", "orders", "Table", "rtype-2", "isPartOf"),
						newRelation("rel-e", "da-100", "Good DA", "Data Attribute",
							"dash-001", "Dashboard", "Dashboard", "rtype-z", "visualizedIn"),
					},
				}
			case "tbl-100":
				// Table has a Column and a non-physical target.
				return http.StatusOK, clients.GetBusinessTermRelationsResponse{
					Total: 2,
					Results: []clients.GetBusinessTermRelation{
						newRelation("rel-f", "tbl-100", "orders", "Table",
							"col-100", "order_id", "Column", "rtype-3", "contains"),
						newRelation("rel-g", "tbl-100", "orders", "Table",
							"idx-001", "pk_orders", "Index", "rtype-w", "indexes"),
					},
				}
			default:
				return http.StatusOK, clients.GetBusinessTermRelationsResponse{Total: 0, Results: []clients.GetBusinessTermRelation{}}
			}
		},
	))

	server := httptest.NewServer(mux)
	defer server.Close()

	client := testutil.NewClient(server)
	output, err := get_business_term.NewTool(client).Handler(t.Context(), get_business_term.Input{
		AssetID: "bt-filter",
	})
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	// Only the Data Attribute should appear in lineage (Report and Data Policy filtered out).
	if len(output.Lineage) != 1 {
		t.Fatalf("Lineage: got %d, want 1 (only Data Attribute)", len(output.Lineage))
	}
	da := output.Lineage[0]
	if da.ID != "da-100" {
		t.Errorf("DA ID: got %q, want %q", da.ID, "da-100")
	}

	// Only the Table should appear (Dashboard filtered out).
	if len(da.Tables) != 1 {
		t.Fatalf("Tables: got %d, want 1 (only Table)", len(da.Tables))
	}
	if da.Tables[0].ID != "tbl-100" {
		t.Errorf("Table ID: got %q, want %q", da.Tables[0].ID, "tbl-100")
	}

	// Only the Column should appear (Index filtered out).
	if len(da.Tables[0].Columns) != 1 {
		t.Fatalf("Columns: got %d, want 1 (only Column)", len(da.Tables[0].Columns))
	}
	if da.Tables[0].Columns[0].ID != "col-100" {
		t.Errorf("Column ID: got %q, want %q", da.Tables[0].Columns[0].ID, "col-100")
	}
}

