// newRelation is a test helper that builds a GetBusinessTermRelation.
func newRelation(id, srcID, srcName, srcType, tgtID, tgtName, tgtType, relID, relName string) clients.GetBusinessTermRelation {
	return clients.GetBusinessTermRelation{
		ID:     id,
		Source: clients.GetBusinessTermRelationAsset{ID: srcID, Name: srcName, Type: clients.GetBusinessTermAssetType{Name: srcType}},
		Target: clients.GetBusinessTermRelationAsset{ID: tgtID, Name: tgtName, Type: clients.GetBusinessTermAssetType{Name: tgtType}},
		Type:   clients.GetBusinessTermRelationType{ID: relID, Name: relName},
	}
}

// setupFullLineageServer creates a test server with a complete lineage chain:
// Business Term (bt-001) → Data Attribute (da-001) → Table (tbl-001) → Column (col-001)
func setupFullLineageServer() *httptest.Server {
	mux := http.NewServeMux()

	mux.Handle("GET /rest/2.0/attributes", testutil.JsonHandlerOut(
		func(r *http.Request) (int, clients.GetBusinessTermAttributesResponse) {
			return http.StatusOK, clients.GetBusinessTermAttributesResponse{
				Total: 3,
				Results: []clients.GetBusinessTermAttributeResponse{
					{
						ID:    "attr-001",
						Type:  clients.GetBusinessTermAttributeType{ID: "atype-1", Name: "Definition"},
						Value: "Total revenue generated by a customer",
					},
					{
						ID:    "attr-002",
						Type:  clients.GetBusinessTermAttributeType{ID: "atype-2", Name: "Note"},
						Value: "Calculated quarterly",
					},
					{
						ID:    "attr-003",
						Type:  clients.GetBusinessTermAttributeType{ID: "atype-3", Name: "Example"},
						Value: "$1,234.56",
					},
				},
			}
		},
	))

	mux.Handle("GET /rest/2.0/assets/{id}", testutil.JsonHandlerOut(
		func(r *http.Request) (int, clients.GetBusinessTermAssetResponse) {
			id := r.PathValue("id")
			if id == "bt-001" {
				return http.StatusOK, clients.GetBusinessTermAssetResponse{
					ID:          "bt-001",
					Name:        "Customer Revenue",
					DisplayName: "Customer Revenue",
					Type:        clients.GetBusinessTermAssetType{ID: "type-bt", Name: "Business Term"},
					Domain:      clients.GetBusinessTermDomain{ID: "dom-001", Name: "Finance"},
					Status:      clients.GetBusinessTermStatus{ID: "st-001", Name: "Approved"},
				}
			}
			return http.StatusNotFound, clients.GetBusinessTermAssetResponse{}
		},
	))

	mux.Handle("GET /rest/2.0/relations", testutil.JsonHandlerOut(
		func(r *http.Request) (int, clients.GetBusinessTermRelationsResponse) {
			sourceID := r.URL.Query().Get("sourceId")
			switch sourceID {
			case "bt-001":
				return http.StatusOK, clients.GetBusinessTermRelationsResponse{
					Total: 1,
					Results: []clients.GetBusinessTermRelation{
						newRelation("rel-1", "bt-001", "Customer Revenue", "Business Term",
							"da-001", "Revenue Attribute", "Data Attribute", "rtype-1", "describes"),
					},
				}
			case "da-001":
				return http.StatusOK, clients.GetBusinessTermRelationsResponse{
					Total: 1,
					Results: []clients.GetBusinessTermRelation{
						newRelation("rel-2", "da-001", "Revenue Attribute", "Data Attribute",
							"tbl-001", "sales_transactions", "Table", "rtype-2", "isPartOf"),
					},
				}
			case "tbl-001":
				return http.StatusOK, clients.GetBusinessTermRelationsResponse{
					Total: 1,
					Results: []clients.GetBusinessTermRelation{
						newRelation("rel-3", "tbl-001", "sales_transactions", "Table",
							"col-001", "revenue_amount", "Column", "rtype-3", "contains"),
					},
				}
			default:
				return http.StatusOK, clients.GetBusinessTermRelationsResponse{Total: 0, Results: []clients.GetBusinessTermRelation{}}
			}
		},
	))

	return httptest.NewServer(mux)
}

func TestGetBusinessTerm_FullLineage(t *testing.T) {
	server := setupFullLineageServer()
	defer server.Close()

	client := testutil.NewClient(server)
	output, err := get_business_term.NewTool(client).Handler(t.Context(), get_business_term.Input{
		AssetID: "bt-001",
	})
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	// Verify asset details.
	if output.ID != "bt-001" {
		t.Errorf("ID: got %q, want %q", output.ID, "bt-001")
	}
	if output.Name != "Customer Revenue" {
		t.Errorf("Name: got %q, want %q", output.Name, "Customer Revenue")
	}
	if output.AssetType != "Business Term" {
		t.Errorf("AssetType: got %q, want %q", output.AssetType, "Business Term")
	}
	if output.DomainID != "dom-001" {
		t.Errorf("DomainID: got %q, want %q", output.DomainID, "dom-001")
	}
	if output.DomainName != "Finance" {
		t.Errorf("DomainName: got %q, want %q", output.DomainName, "Finance")
	}
	if output.StatusName != "Approved" {
		t.Errorf("StatusName: got %q, want %q", output.StatusName, "Approved")
	}

	// Verify attributes.
	if len(output.Attributes) != 3 {
		t.Fatalf("Attributes: got %d, want 3", len(output.Attributes))
	}
	wantAttrs := []struct{ name, value string }{
		{"Definition", "Total revenue generated by a customer"},
		{"Note", "Calculated quarterly"},
		{"Example", "$1,234.56"},
	}
	for i, want := range wantAttrs {
		if output.Attributes[i].Name != want.name {
			t.Errorf("Attributes[%d].Name: got %q, want %q", i, output.Attributes[i].Name, want.name)
		}
		if output.Attributes[i].Value != want.value {
			t.Errorf("Attributes[%d].Value: got %q, want %q", i, output.Attributes[i].Value, want.value)
		}
	}

	// Verify lineage: BT → DA → Table → Column.
	if len(output.Lineage) != 1 {
		t.Fatalf("Lineage: got %d data attributes, want 1", len(output.Lineage))
	}

	da := output.Lineage[0]
	if da.ID != "da-001" {
		t.Errorf("DA ID: got %q, want %q", da.ID, "da-001")
	}
	if da.Name != "Revenue Attribute" {
		t.Errorf("DA Name: got %q, want %q", da.Name, "Revenue Attribute")
	}
	if da.RelationType != "describes" {
		t.Errorf("DA RelationType: got %q, want %q", da.RelationType, "describes")
	}

	if len(da.Tables) != 1 {
		t.Fatalf("Tables: got %d, want 1", len(da.Tables))
	}
	table := da.Tables[0]
	if table.ID != "tbl-001" {
		t.Errorf("Table ID: got %q, want %q", table.ID, "tbl-001")
	}
	if table.Name != "sales_transactions" {
		t.Errorf("Table Name: got %q, want %q", table.Name, "sales_transactions")
	}
	if table.RelationType != "isPartOf" {
		t.Errorf("Table RelationType: got %q, want %q", table.RelationType, "isPartOf")
	}

	if len(table.Columns) != 1 {
		t.Fatalf("Columns: got %d, want 1", len(table.Columns))
	}
	col := table.Columns[0]
	if col.ID != "col-001" {
		t.Errorf("Column ID: got %q, want %q", col.ID, "col-001")
	}
	if col.Name != "revenue_amount" {
		t.Errorf("Column Name: got %q, want %q", col.Name, "revenue_amount")
	}
	if col.RelationType != "contains" {
		t.Errorf("Column RelationType: got %q, want %q", col.RelationType, "contains")
	}
}

